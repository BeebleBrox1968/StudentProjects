print("What language do you speak?")
choice = input()
if choice == 'Spanish':
    print("Hola!")
elif choice == 'English':
    print("Hello!")
elif choice == 'Mandarin':
    print("Ni hao!")
elif choice == 'Japanese':
    print("Konichiwa!")
else:
    print("I'm sorry but I don't know that language...")
